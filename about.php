            <h2 class="fw-bold text-center">Library Management System</h2>
            <p class="mt-4">A Library management system is a software that uses to maintain the record of the library.
                It contains work like the number of available books in the library, the number of books are issued or
                returning or renewing a book, etc. Library Management Systems is software
                that helps to maintain a database that is useful to enter new books & record books borrowed by the
                members, with the respective submission dates. Moreover, it also reduces the manual record burden of the
                librarian.
                <br><br>
                Library management system allows the librarian to maintain library resources in a more operative manner
                that will help to save their time. It is also convenient for the librarian to manage the process of
                books allotting and making payment. Library management system is also useful for students as well as a
                librarian to keep the constant track of the availability of all books in a store.
            </p>

            <p><b>Importance of library management system</b></p>

